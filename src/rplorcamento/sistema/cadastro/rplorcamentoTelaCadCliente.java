    public void SalvarClientes() throws ClassNotFoundException {

        ArrayList<String> Registro = new ArrayList<>();
        Registro.add(jText_codigoCliente.getText());
        Registro.add(jText_nomeRazaoCliente.getText());
        Registro.add(jText_RGIECliente.getText());
        Registro.add(jText_fantasiaApelidoCliente.getText());
        Registro.add(jText_CPFCNPJCliente.getText());
        Registro.add(jText_enderecoCliente.getText());
        Registro.add(jText_bairroCliente.getText());
        Registro.add(jText_cidadeCliente.getText());
        Registro.add(jText_CEPCliente.getText());
        String uf = (String) jText_UFCliente.getSelectedItem();
        Registro.add(uf);
        Registro.add(jText_telefoneCliente.getText());
        Registro.add(jText_celularCliente.getText());
        Registro.add(jText_emailCliente.getText());
        Registro.add(jText_referenciaCliente.getText());
        if ("  /  /    ".equals(jText_nascimentoCliente.getText())) {
            Registro.add("00/00/0000");
        } else {
            Registro.add(jText_nascimentoCliente.getText());
        }
        Registro.add(jText_observacaoCliente.getText());

        rplorcamentoClienteControl ControllerCidade = new rplorcamentoClienteControl();
        try {
            //
            ControllerCidade.Salvar(Registro);
            //

// TODO add your handling code here:
        } catch (ParseException ex) {

            JOptionPane.showMessageDialog(null, "Erro Fatal! Erro, " + ex + ". Informações inseridas incorretamentes!");

            Logger.getLogger(rplorcamentoTelaCadCliente.class.getName()).log(Level.SEVERE, null, ex);
        }

        LimparCampos();

    }

