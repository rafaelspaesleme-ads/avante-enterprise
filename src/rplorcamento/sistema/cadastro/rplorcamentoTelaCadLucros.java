    private void jButton_calcular_vendaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_calcular_vendaActionPerformed

        double qtd_venda = Double.parseDouble(jText_qtd_venda.getValue().toString().replace(",", "."));
        double vl_unitario = Double.parseDouble(jText_vlUnitario_venda.getText().replace(",", "."));
        double vl_total = qtd_venda * vl_unitario;
        double vl_final = (vl_total - VL_DESCONTO) + VL_IMPOSTO;
        jText_vlTotal_venda.setText(String.valueOf(vl_final).replace(".", ","));
        QTD_ATALIZADA_PARA_ESTOQUE = (QUANTIDADE_VINDA_DO_BANCO - qtd_venda);

// TODO add your handling code here:
    }//GEN-LAST:event_jButton_calcular_vendaActionPerformed

