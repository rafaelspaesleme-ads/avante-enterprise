    public void CarregaInfoClientes() {

        try {

            Connection conn = rplorcamentoConexao.GeraConexao();

            String sql = "SELECT * FROM bd_rplorcamento.clientes";

            PreparedStatement comando = conn.prepareStatement(sql);

            ResultSet rs = comando.executeQuery();
//          cbx_id_pes_usuario.removeAllItems();

            jList_clientes_proposta.removeAll();
            jList_clientes_os.removeAll();
            jList_clientes_recibo.removeAll();
            jList_clientes_contasreceber.removeAll();

            ArrayList<String> lista = new ArrayList<String>();

            while (rs.next()) {

                lista.add(rs.getString("nome_fantasia_apelido"));

            }

            Iterator it = lista.iterator();
            DefaultListModel model = new DefaultListModel();
            jList_clientes_proposta.setModel(model);
            jList_clientes_os.setModel(model);
            jList_clientes_recibo.setModel(model);
            jList_clientes_contasreceber.setModel(model);
            while (it.hasNext()) {
                String itemLista = (String) it.next();
                model.addElement(itemLista);
            }

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, e);
        }

    }

