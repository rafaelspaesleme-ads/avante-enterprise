    public void SalvarTudo() {

        if (jChbx_servicosExtras.isSelected() == true) {

            SalvarSalario();

            if ((!"".equals(jText_tipoVeiculo.getText()))) {
                SalvarTransporte();
            }

            if ((!"0".equals(jText_vlTotalHospedagem.getText())) && ("0".equals(jText_vlTotalAlimentacao.getText()))) {
                SalvarHospedagem();
            } else if (("0".equals(jText_vlTotalHospedagem.getText())) && (!"0".equals(jText_vlTotalAlimentacao.getText()))) {
                SalvarAlimentacao();
            } else if ((!"0".equals(jText_vlTotalHospedagem.getText())) && (!"0".equals(jText_vlTotalAlimentacao.getText()))) {
                SalvarHospedagem();
                SalvarAlimentacao();
            } else {
                JOptionPane.showMessageDialog(null, "Sem informações de Hospedagem/Alimentação para cadastrar.");
            }

            if (!"".equals(jText_vlTotalPagar.getText())) {
                SalvarOutros();
            }
            
            this.dispose();
            new rplorcamentoTelaCadGastos().setVisible(true);

        } else {
            JOptionPane.showMessageDialog(null, "Você não pode cadastrar informações extras de contas a pagar, \npois não há um funcionário relacionado a esta proposta.");
        }
        
        

    }

    public void SalvarOutros() {

        try {

            Connection conn = rplorcamentoConexao.GeraConexao();

            String sql = "insert into bd_rplorcamento.outros_gastos values(?,?,?,?,?,?,?,?,?,?,?,?,?)";

            PreparedStatement comando = conn.prepareStatement(sql);

            comando.setInt(1, ID_ALIMENTACAO);
            comando.setInt(2, ID_FUNCIONARIO);
            comando.setString(3, NOME_FUNCIONARIO_OUTROS);
            comando.setString(4, jText_descricaoPagar.getText());
            comando.setString(5, jText_empresaPessoaPagar.getText());
            comando.setString(6, (String) jCbx_formaPagamento.getSelectedItem());
            comando.setString(7, jText_NumDocumentoPagar.getText());

            String data_emissao = jText_emissaoConta.getText();
            DateFormat fmt = new SimpleDateFormat("dd/MM/yyyy");
            java.sql.Date data1 = new java.sql.Date(fmt.parse(data_emissao).getTime());
            comando.setDate(8, data1);

            String data_vencimento = jText_vencimentoConta.getText();
            DateFormat fmt1 = new SimpleDateFormat("dd/MM/yyyy");
            java.sql.Date data2 = new java.sql.Date(fmt1.parse(data_vencimento).getTime());
            comando.setDate(9, data2);

            String data_pagamento = jText_pagamentoConta.getText();
            DateFormat fmt2 = new SimpleDateFormat("dd/MM/yyyy");
            java.sql.Date data3 = new java.sql.Date(fmt2.parse(data_pagamento).getTime());
            comando.setDate(10, data3);

            comando.setDouble(11, Double.parseDouble(jText_vlUnitarioPagar.getText().replace(",", ".")));
            comando.setDouble(12, Double.parseDouble(jText_qtdPagar.getValue().toString().replace(",", ".")));
            comando.setDouble(13, Double.parseDouble(jText_vlTotalPagar.getText().replace(",", ".")));

            //Execulta o comando no banco
            comando.executeUpdate();

            //Fechar as conecções
            comando.close();
            conn.close();

            JOptionPane.showMessageDialog(null, "Valores de outras contas cadastrados com sucesso!");

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, e);
        }

    }

