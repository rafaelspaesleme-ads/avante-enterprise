    public void CalcularGastosComVeiculos() {

        double vl_unitario_combustivel = Double.parseDouble(jText_vlUnitarioCombustivel.getText().replace(",", "."));
        double quant_combustivel = Double.parseDouble(jText_quantidadeCombustivel.getValue().toString().replace(",", "."));
        double vl_estacionamento = Double.parseDouble(jText_vlEstacionamento.getText().replace(",", "."));
        double vl_manutencao = Double.parseDouble(jText_vlmanutencao.getText().replace(",", "."));
        double quant_diasdeuso = Double.parseDouble(jText_diasDeUso.getValue().toString().replace(",", "."));
        double quant_paradas = Double.parseDouble(jText_quantParadas.getValue().toString().replace(",", "."));
        double quant_manutencao = Double.parseDouble(jText_quantManutencao.getValue().toString().replace(",", "."));
        double vl_pedagio = Double.parseDouble(jText_vlPedagio.getText().replace(",", "."));
        double vl_pneu = Double.parseDouble(jText_vlPneu.getText().replace(",", "."));
        double outrosGastos = Double.parseDouble(jText_outrosGastos.getText().replace(",", "."));
        double quant_pedagio = Double.parseDouble(jText_quantPedagio.getValue().toString().replace(",", "."));
        double IPVA = Double.parseDouble(jText_IPVA.getText().replace(",", "."));

        double valor_dos_itens = (vl_unitario_combustivel*quant_combustivel) + (vl_pedagio*quant_pedagio) + (vl_manutencao*quant_manutencao) + (vl_estacionamento*quant_paradas);
        double gastos_extras = vl_pneu + outrosGastos + IPVA;
        double valor_total = quant_diasdeuso * (valor_dos_itens + gastos_extras);

        jText_vlFinalGastosVeiculo.setText(String.valueOf(valor_total).replace(".", ","));

    }

