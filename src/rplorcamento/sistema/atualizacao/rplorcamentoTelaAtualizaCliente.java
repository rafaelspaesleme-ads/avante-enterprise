    public void ExibirCodigoCliente() {

        String buscarNome = (String) jCbx_cod_cliente.getSelectedItem();

        try {

            if (!buscarNome.equals("")) {

                LimparCampos();

                Connection conn = rplorcamentoConexao.GeraConexao();

                String sql = "SELECT * FROM bd_rplorcamento.clientes WHERE id = ?";

                PreparedStatement comando = conn.prepareStatement(sql);

                comando.setString(1, buscarNome);

                ResultSet rs = comando.executeQuery();

                while (rs.next()) {

                    jText_codigoCliente.setText(rs.getString("id"));
                    jText_nomeRazaoCliente.setText(rs.getString("nome_razao_social"));
                    jText_RGIECliente.setText(rs.getString("rg_ie"));
                    jText_fantasiaApelidoCliente.setText(rs.getString("nome_fantasia_apelido"));
                    jText_CPFCNPJCliente.setText(rs.getString("cpf_cnpj"));
                    jText_enderecoCliente.setText(rs.getString("endereco"));
                    jText_bairroCliente.setText(rs.getString("bairro"));
                    jText_cidadeCliente.setText(rs.getString("cidade"));
                    jText_CEPCliente.setText(rs.getString("cep"));
                    jText_UFCliente.setSelectedItem(rs.getString("uf"));
                    jText_telefoneCliente.setText(rs.getString("telefone"));
                    jText_celularCliente.setText(rs.getString("celular"));
                    jText_emailCliente.setText(rs.getString("email"));
                    jText_referenciaCliente.setText(rs.getString("referencia"));

                    String data_nascimento = rs.getString("nascimento");
                    DateFormat fmt2 = new SimpleDateFormat("yyyy-MM-dd");
                    java.util.Date data2 = new java.util.Date(fmt2.parse(data_nascimento).getTime());

                    SimpleDateFormat format_ = new SimpleDateFormat("dd/MM/yyyy");
                    String dataFormatada_ = format_.format(data2);

                    jText_nascimentoCliente.setText(String.valueOf(dataFormatada_));

                    jText_observacaoCliente.setText(rs.getString("observacao"));

                }

            } else {
                JOptionPane.showMessageDialog(null, "Selecione um cliente!");
            }

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, e);
        }

    }

