    public void CalcularVlParcial() {

        Double quantidade = Double.parseDouble(jText_quantidade.getValue().toString().replace(",", "."));
        Double vl_unitario = Double.parseDouble(jText_vlUnitario.getText().replace(",", "."));
        Double vl_total = quantidade * vl_unitario;
        String strg_vl_total = String.valueOf(vl_total);

        jText_valorTotal.setText(strg_vl_total.replace(".", ","));

    }

    public void CalcularVlFinal() {

        Double vl_total = Double.parseDouble(jText_valorTotal.getText().replace(",", "."));
        Double vl_desconto = Double.parseDouble(jText_vlDesconto.getText().replace(",", "."));
        Double vl_imposto = Double.parseDouble(jText_vlImpostos.getText().replace(",", "."));
        Double vl_final = (vl_total - vl_desconto) + vl_imposto;
        String strg_vl_final = String.valueOf(vl_final);

        jText_vlFinal.setText(strg_vl_final.replace(".", ","));

    }

