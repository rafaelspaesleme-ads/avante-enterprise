    public void CarregarTela() {
        try {
            Connection conn = rplorcamentoConexao.GeraConexao();

            String sql = "SELECT * FROM bd_rplorcamento.clientes";

            PreparedStatement comando = conn.prepareStatement(sql);

            ResultSet rs = comando.executeQuery();

            DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
            modelo.setNumRows(0);

            while (rs.next()) {

//                    String dataFormatada_ = format_.format(rs.getString("nascimento"));
                modelo.addRow(
                        new Object[]{
                            rs.getString("id"),
                            rs.getString("nome_razao_social"),
                            rs.getString("rg_ie"),
                            rs.getString("nome_fantasia_apelido"),
                            rs.getString("cpf_cnpj"),
                            rs.getString("endereco"),
                            rs.getString("bairro"),
                            rs.getString("cidade"),
                            rs.getString("cep"),
                            rs.getString("uf"),
                            rs.getString("telefone"),
                            rs.getString("celular"),
                            rs.getString("email"),
                            rs.getString("referencia"),
                            format_.format(rs.getDate("nascimento")),
                            rs.getString("email")
                        }
                );

            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Problemas ao tentar conectar com o banco de dados: " + e);
        }
    }

